package com.travel.Entity;

public class Guide {

	private int guide_id;
	private String guide_name;
	private String guide_phone;
	private String guide_email;
	private String guide_age;
	private String location;
	private String pass;
	
	
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getGuide_id() {
		return guide_id;
	}
	public void setGuide_id(int guide_id) {
		this.guide_id = guide_id;
	}
	public String getGuide_name() {
		return guide_name;
	}
	public void setGuide_name(String guide_name) {
		this.guide_name = guide_name;
	}
	public String getGuide_phone() {
		return guide_phone;
	}
	public void setGuide_phone(String guide_phone) {
		this.guide_phone = guide_phone;
	}
	public String getGuide_email() {
		return guide_email;
	}
	public void setGuide_email(String guide_email) {
		this.guide_email = guide_email;
	}
	public String getGuide_age() {
		return guide_age;
	}
	public void setGuide_age(String guide_age) {
		this.guide_age = guide_age;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
	
	
}
